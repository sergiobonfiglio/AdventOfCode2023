package main

import "testing"

func TestPart1(t *testing.T) {
	tests := []struct {
		name  string
		input string
		want  any
	}{
		{
			name: "example",
			input: `broadcaster -> a, b, c
%a -> b
%b -> c
%c -> inv
&inv -> a
`,
			want: 32000000,
		},
		{
			name: "example2",
			input: `broadcaster -> a
%a -> inv, con
&inv -> b
%b -> con
&con -> output
`,
			want: 11687500,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := part1(tt.input); got != tt.want {
				t.Errorf("part1() = %v, want %v", got, tt.want)
			}
		})
	}
}

